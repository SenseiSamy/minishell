#include "minishell.h"

int	ft_unset(char **args)
{
	int		i;

	i = 0;
	while (args[++i])
	{
		if (ft_strcmp(args[i], "?") == 0)
			continue ;
		env_delone(args[i]);
	}
	return (0);
}
